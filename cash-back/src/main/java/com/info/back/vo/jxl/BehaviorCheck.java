package com.info.back.vo.jxl;

public class BehaviorCheck {
	private String check_point;
	private Integer score;
	private String check_point_cn;
	private String result;
	private String evidence;

	public String getCheck_point() {
		return check_point;
	}

	public void setCheck_point(String checkPoint) {
		check_point = checkPoint;
	}

	public Integer getScore() {
		return score;
	}

	public void setScore(Integer score) {
		this.score = score;
	}

	public String getCheck_point_cn() {
		return check_point_cn;
	}

	public void setCheck_point_cn(String checkPointCn) {
		check_point_cn = checkPointCn;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public String getEvidence() {
		return evidence;
	}

	public void setEvidence(String evidence) {
		this.evidence = evidence;
	}

}

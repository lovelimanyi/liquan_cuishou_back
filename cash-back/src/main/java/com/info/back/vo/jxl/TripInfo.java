package com.info.back.vo.jxl;

public class TripInfo {
	private String trip_dest;
	private String trip_leave;
	private String trip_end_time;
	private String trip_type;
	private String trip_start_time;

	public String getTrip_dest() {
		return trip_dest;
	}

	public void setTrip_dest(String tripDest) {
		trip_dest = tripDest;
	}

	public String getTrip_leave() {
		return trip_leave;
	}

	public void setTrip_leave(String tripLeave) {
		trip_leave = tripLeave;
	}

	public String getTrip_end_time() {
		return trip_end_time;
	}

	public void setTrip_end_time(String tripEndTime) {
		trip_end_time = tripEndTime;
	}

	public String getTrip_type() {
		return trip_type;
	}

	public void setTrip_type(String tripType) {
		trip_type = tripType;
	}

	public String getTrip_start_time() {
		return trip_start_time;
	}

	public void setTrip_start_time(String tripStartTime) {
		trip_start_time = tripStartTime;
	}

}
